import UIKit

// 1
func addTwoNumbers(number1: Double, number2: Double) -> Double {
    return number1 + number2
}

// 2
func subtractNumber(total: Int, number: Int) -> Int {
    return total - number
}

// 3
func multiplyTwoNumbers(number1: Float, number2: Float) -> Float {
    return number1 * number2
}

// 4
func divideTwoNumbers(number1: Double, number2: Double) -> Double {
    return number1 / number2
}


